## ** Description

## 'myLib.R' is a reference implementation, illustrating the conventions for
## writing R packages with Org-mode. It shows the `outshine' approach, i.e.
## development is actually done in a machine-executable =.R= file that is only
## temporarily converted to Org-mode for comment-text editing and file export (to
## text formatting backends or as R package).

